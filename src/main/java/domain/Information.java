package domain;

public class Information {
    private String informationName;
    private String informationImage;
    private String informationChapter;

    public String getInformationName() {
        return informationName;
    }

    public String getInformationImage() {
        return informationImage;
    }

    public String getInformationChapter() {
        return informationChapter;
    }

    public void setInformationName(String informationName) {
        this.informationName = informationName;
    }

    public void setInformationImage(String informationImage) {
        this.informationImage = informationImage;
    }

    public void setInformationChapter(String informationChapter) {
        this.informationChapter = informationChapter;
    }
}
