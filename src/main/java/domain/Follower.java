package domain;

public class Follower {
    private String followersName;
    private String followersImage;

    public String getFollowersName() {
        return followersName;
    }

    public void setFollowersName(String followersName) {
        this.followersName = followersName;
    }

    public String getFollowersImage() {
        return followersImage;
    }

    public void setFollowersImage(String followersImage) {
        this.followersImage = followersImage;
    }
}
